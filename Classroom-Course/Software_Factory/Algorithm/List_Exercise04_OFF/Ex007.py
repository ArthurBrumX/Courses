# 7 - Faça um algoritmo que calcule a area de um triangulo.
# Posteriormente imprima o resultado!

print("=" * 60)
print("*** Calculadora De Triangulo! ***")
print("*** Bem-Vindo Usuário(a)! ***")
print("=" * 60)

print("* Vamos Calcular a Área De Um Triangulo! *")
print("=" * 60)

base = float(input("- Digite a Base Do Triangulo: "))
altura = float(input("- Digite a Altura Do triangulo: "))

area = (base * altura) / 2

print(" - A Área Do Trinagulo é: {}".format(area))

print("=" * 60)
print("*** Fim Do Calculo! ***")
print("=" * 60)


